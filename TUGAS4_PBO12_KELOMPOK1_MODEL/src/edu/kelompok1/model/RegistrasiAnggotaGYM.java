public class RegistrasiAnggotaGYM {
    private String noKTP;
     private String nama;
     private String jenisKelamin;
     private String cabang;
     private String handuk;
     private String paketFitnes;

    public String getNoKTP() {
        return noKTP;
    }

    public void setNoKTP(String noKTP) {
        this.noKTP = noKTP;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJenisKelamin() {
        return jenisKelamin;
    }

    public void setJenisKelamin(String jenisKelamin) {
        this.jenisKelamin = jenisKelamin;
    }

    public String getCabang() {
        return cabang;
    }

    public void setCabang(String cabang) {
        this.cabang = cabang;
    }

    public String getHanduk() {
        return handuk;
    }

    public void setHanduk(String handuk) {
        this.handuk = handuk;
    }

    public String getPaketFitnes() {
        return paketFitnes;
    }

    public void setPaketFitnes(String paketFitnes) {
        this.paketFitnes = paketFitnes;
    }
     
}
